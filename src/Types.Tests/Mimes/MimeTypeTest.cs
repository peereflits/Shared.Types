    public void WhenGetByWithKnownExtension_ItShouldReturnAnInstance(string extension)
    {
        MimeType result = MimeType.GetBy(extension);
        Assert.False(string.IsNullOrEmpty(result.Mime));
        Assert.Equal(extension, result.Extension);
    }

    [Theory]
    [InlineData(" .zip")]
    [InlineData(".zip ")]
    [InlineData(" .zip ")]
    [InlineData(".ZIP")]
    public void WhenGetByWithNonCleanedExtension_ItShouldReturnAnInstance(string extension)
    {
        MimeType result = MimeType.GetBy(extension);
        Assert.False(string.IsNullOrEmpty(result.Mime));
        Assert.Equal(".zip", result.Extension);
    }

    [Theory]
    [InlineData(" ")]
    [InlineData(default(string))]
    [InlineData(".unknown")]
    public void WhenGetByWithUnknownExtension_ItShouldReturnAnEmptyInstance(string extension)
    {
        MimeType result = MimeType.GetBy(extension);
        Assert.NotNull(result);
        Assert.Equal(string.Empty, result.Mime);
        Assert.Equal(string.Empty, result.Extension);
    }
